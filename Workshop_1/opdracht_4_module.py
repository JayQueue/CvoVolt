import math

def oppervlakte_rechthoek(lengte, breedte):
    return lengte*breedte

def omtrek_rechthoek(lengte, breedte):
    return 2*(lengte+breedte)

def omtrek_cirkel(straal):
    return 2*math.pi*straal

def oppervlakte_vierkant(zijde):
    return zijde**2
